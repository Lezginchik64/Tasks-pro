# == Модуль copy ==
# Модуль copy содержит две функции:
    # copy() — копирует объект и возвращает его поверхностную копию
    # deepcopy() — копирует объект и возвращает его глубокую копию

# == Поверхностное копирование ==
# Поверхностное копирование создает отдельный новый объект, но вместо копирования дочерних элементов в новый объект оно просто копирует ссылки на их адреса памяти.
import copy

data1 = [1, 2, 3]
data2 = copy.copy(data1)
data1.append(4)
print(id(data1), data1)     # 4307472512 [1, 2, 3, 4]
print(id(data2), data2)     # 4307470720 [1, 2, 3]
# Всегда выводит разные идентификаторы, так как переменная data2 ссылается на новый объект, который представляет копию списка [1, 2, 3].
# В данном примере элементами списка являются целые числа (неизменяемый тип int), поэтому изменение одного списка не отражается на другом.
# Если бы элементами списка были изменяемые типы, то поверхностное копирование скопировало бы лишь ссылки на их адреса памяти.
# Следовательно, любое изменение элементов одного объекта отразилось бы также на элементах другого объекта.
import copy

data1 = [[1, 2, 3], [4, 5, 6]]
data2 = copy.copy(data1)
data1[0].append(7)
data2[1].append(8)
print(id(data1), data1)     # 4303669248 [[1, 2, 3, 7], [4, 5, 6, 8]]
print(id(data2), data2)     # 4302278784 [[1, 2, 3, 7], [4, 5, 6, 8]]
print(id(data1[0]))     # 4305266560
print(id(data2[0]))     # 4305266560
# Сами списки, это теперь два разных объекта, так как второй создан методом copy, но списки внутри списков (data1[0]) ссылаются на один и тот же объект, поэтому при изменении одного списка, изменяется и второй список (не объект)
# Изменения затронули оба списка, потому что оба они содержат ссылки на один и тот же вложенный объект. Так работает поверхностное копирование.
# Поверхностное копирование создает отдельный новый объект, но вместо того чтобы копировать внутренние элементы в новый объект, оно просто копирует ссылки на их адреса памяти.


# == Глубокое копирование ==
# Глубокое копирование создает новую отдельную копию всего объекта со своим уникальным адресом памяти.
# Это означает, что любые изменения, внесенные вами в новую копию объекта, не будут отражаться в исходной и наоборот.
data1 = [[1, 2, 3], [4, 5, 6]]
data2 = copy.deepcopy(data1)
data1[0].append(7)
data2[1].append(8)
print(id(data1), data1)     # 4332436928 [[1, 2, 3, 7], [4, 5, 6]]
print(id(data2), data2)     # 4332455936 [[1, 2, 3], [4, 5, 6, 8]]
print(id(data1[0]))     # 4331063680
print(id(data2[0]))     # 4333336704
# При глубоком копировании временные затраты на создание копии, очевидно, выше, чем при создании поверхностной копии.
# Если копируемый объект имеет сложную вложенную структуру, то глубокое копирование может тратить много времени.


