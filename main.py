# Примечание 3. Поверхностную копию списка также можно создать с помощью среза всего списка.
data = [1, 2, 3, 4]
new_data = data[:]
print(data is new_data, data == new_data)   # False True

# Примечание 5. Для определения размера объектов встроенных типов можно использовать функцию getsizeof() модуля sys.
# Данная функция возвращает размер объекта в байтах.
import sys

print(sys.getsizeof(10))        # 28
print(sys.getsizeof(True))      # 28
print(sys.getsizeof(None))      # 16
print(sys.getsizeof(''))        # 51
print(sys.getsizeof('beegeek'))     # 56

# Обратите внимание на то, что с помощью функции getsizeof() нельзя вычислять размер сложных объектов, содержащих вложенные структуры (списки списков и так далее).
# Для того чтобы правильно определять размер абсолютно любого объекта (включая пользовательские), в Python используется функция asizeof() модуля asizeof, который находится в библиотеке pympler.