def funk(d1, d2, d3):
    m1 = d1 + d2 + d3
    m2 = 2 * d1 + 2 * d2
    m3 = 2 * d2 + 2 * d3
    m4 = 2 * d1 + 2 * d3
    return min(m1, m2, m3, m4)


d1, d2, d3 = int(input()), int(input()), int(input())
print(funk(d1, d2, d3))

