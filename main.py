import shutil  # модуль для высокоуровневых операций с файлами и директориями
'----------------------'
shutil.copy(src, dst)
# копирует файл src → dst (сохраняет имя, но не метаданные)     !
shutil.copy2(src, dst)
# как copy, но сохраняет метаданные (время создания, модификации и т.п.)
shutil.copyfile(src, dst)
# копирует только содержимое файла (без проверки существования dst)
shutil.copytree(src, dst)
# копирует всю директорию (рекурсивно) src → dst
'----------------------'
shutil.move(src, dst)
# перемещает файл или папку (как os.rename, но работает и между дисками)        !
shutil.rmtree(path)
# удаляет папку и всё её содержимое (рекурсивно)            !
'----------------------'
shutil.disk_usage('путь')
# возвращает (total, used, free) дискового пространства в байтах     !
'----------------------'
shutil.chown(path, user=None, group=None)
# изменяет владельца файла (только на Unix)
'----------------------'
shutil.make_archive('base_name', 'format', root_dir)
# создаёт архив (zip, tar, gztar, bztar, xztar) из директории root_dir      !
# format — один из: 'zip', 'tar', 'gztar', 'bztar', 'xztar'
shutil.unpack_archive('archive.zip', extract_dir=None)
# распаковывает архив в указанную директорию (или в текущую)
'----------------------'
shutil.get_archive_formats()
# список доступных форматов архивации
shutil.get_unpack_formats()
# список доступных форматов распаковки
'----------------------'
shutil.which('имя_программы')
# возвращает путь к исполняемому файлу (если найден в PATH), иначе None
'----------------------'
# ПРИМЕЧАНИЯ:
# shutil — безопасный способ работы с файлами и директориями (в т.ч. между дисками)
# shutil.copytree() требует, чтобы dst не существовал
# Для фильтрации файлов при копировании — использовать параметр ignore (например, ignore=shutil.ignore_patterns('*.txt'))
# shutil.rmtree() — аккуратнее: безвозвратно удаляет всё содержимое!

